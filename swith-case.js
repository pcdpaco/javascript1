// switch case

let tipoTarjeta = "debito";

switch (tipoTarjeta) {
    case "debito":
        console.log("Esto es una tarjeta de débito");
        break;
    case "Credit Card":
        console.log("Esta es una tarjeta de crédito");
        break;
    default:
        console.log("Eror de tarjeta");
        break;
}